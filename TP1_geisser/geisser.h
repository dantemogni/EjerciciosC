/**\brief Esta función suma dos valores flotantes devolviendo el resultado.
 *
 * \param sumando1 de tipo flotante, es el primer término de la suma.
 * \param sumando2 de tipo flotante, es el segundo término de la suma.
 * \return devuelve rtaFuncionSum de tipo flotante, resultado de la suma de los parámetros que recibe.
 *
 */
float sumar(float sumando1, float sumando2);

/**\brief Esta función resta dos valores flotantes devolviendo el resultado.
 *
 * \param minuendo de tipo flotante, es el primer término de la resta.
 * \param sustraendo de tipo flotante, es el segundo término de la resta.
 * \return devuelve rtaFuncionRes de tipo flotante, resultado de la resta de los parámetros que recibe.
 *
 */
float restar(float minuendo, float sustraendo);

/**\brief Esta función multiplica dos valores flotantes devolviendo el resultado.
 *
 * \param factor1 de tipo flotante, es el primer factor del producto.
 * \param factor2 de tipo flotante, es el segundo factor del producto.
 * \return devuelve rtaFuncionMulti de tipo flotante, resultado del producto de los parámetros que recibe.
 *
 */
float multiplicar(float factor1, float factor2);

/**\brief Esta función divide dos valores flotantes devolviendo el resultado.
 *
 * \param dividendo de tipo flotante, es el dividendo de la división.
 * \param divisor de tipo flotante, es el divisor de la división.
 * \return devuelve rtaFuncionDiv de tipo flotante, resultado de la división de los parámetros que recibe.
 *
 */
float dividir(float dividendo, float divisor);

/**\brief Esta función obtiene el factorial de un número y lo devuelve.
 *
 * \param operandoFac de tipo entero, es el número del cual se calcula el factorial.
 * \return devuelve rtaFuncionFactor de tipo entero, factorial de operandoFac.
 *
 */
int factorial(int operandoFac);
